<template>
   <div class="author">
      <span :style="{color:color}" class="name">{{autor}}</span>
   </div>
</template>

<script>
import { mapState } from 'vuex';

export default {
    name: 'Author',
    props: {
        senderObj: {
            type: Object,
            required: true
        },
        color: {
            type: String,
            required: true
        }
    },
    computed: {
        ...mapState(['self']),

        autor () {
            if (this.senderObj.name) {
                return this.senderObj.name;
            } else if (this.senderObj.id === this.self.id) {
                return 'VocÃª';
            }
            return '+' + this.senderObj.id.replace('@c.us', '');
        }
    }
};
</script>

